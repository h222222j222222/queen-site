export function getLocalDateKey() {
  const now = new Date();
  const year = now.getFullYear();
  const month = (now.getMonth() + 1 < 10 ? "0" : "") + (now.getMonth() + 1);
  const day = (now.getDate() < 10 ? "0" : "") + now.getDate();
  return `${year}-${month}-${day}`;
}

