export function getRandomIndex(length, previousIndex = -1) {
  if (length <= 1) {
    return 0;
  }
  let nextIndex = Math.floor(Math.random() * length);
  while (nextIndex === previousIndex) {
    nextIndex = Math.floor(Math.random() * length);
  }
  return nextIndex;
}

